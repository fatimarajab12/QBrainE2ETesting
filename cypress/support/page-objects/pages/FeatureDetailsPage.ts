// Feature Details Page - Page Object Model
// Contains:
// - selectors: Define all page elements (status dropdown, test cases list, bugs list, etc.)
// - visit(featureId): Navigate to feature details page
// - verifyFeatureName(name): Verify feature name
// - verifyFeatureStatus(status): Verify feature status
// - clickStatusDropdown(): Click status dropdown
// - selectStatus(status): Select status from dropdown (pending, in_progress, completed, blocked)
// - verifyStatusUpdated(newStatus): Verify status updated
// - verifyTestCaseCounts(): Verify test case counts
// - verifyBugCounts(): Verify bug counts
// - clickCreateTestCase(): Click create test case button
// - clickCreateBug(): Click create bug button

