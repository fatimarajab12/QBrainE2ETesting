// Contains:
// - createTestCase(token, testCaseData): Create new test case
// - getTestCases(token, featureId): Get all test cases for feature
// - getTestCasesByProject(token, projectId): Get all test cases for project
// - getTestCase(token, testCaseId): Get specific test case
// - updateTestCase(token, testCaseId, updateData): Update test case
// - updateTestCaseStatus(token, testCaseId, status): Update test case status only (pending, in_progress, passed, failed, blocked)
// - deleteTestCase(token, testCaseId): Delete test case
// - getTestCaseCounts(token, featureId): Get test case counts
// - generateTestCases(token, featureId, options): Generate test cases using AI

