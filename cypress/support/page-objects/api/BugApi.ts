// Bug API Object - Page Object Model
// Contains:
// - createBug(token, bugData): Create new bug
// - getBugs(token, projectId): Get all project bugs
// - getBugsByFeature(token, featureId): Get all feature bugs
// - getBug(token, bugId): Get specific bug
// - updateBug(token, bugId, updateData): Update bug
// - updateBugStatus(token, bugId, status): Update bug status only (Open, In Progress, Resolved, Closed)
// - deleteBug(token, bugId): Delete bug
// - getBugCounts(token, projectId): Get bug counts
// - getBugStats(token, projectId): Get bug statistics

