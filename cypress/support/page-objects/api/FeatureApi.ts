// Feature API Object - Page Object Model
// Contains:
// - createFeature(token, featureData): Create new feature
// - getFeatures(token, projectId): Get all project features
// - getFeature(token, featureId): Get specific feature
// - updateFeature(token, featureId, updateData): Update feature
// - updateFeatureStatus(token, featureId, status): Update feature status only (pending, in_progress, completed, blocked)
// - deleteFeature(token, featureId): Delete feature
// - getFeatureCounts(token, featureId): Get feature counts (test cases, bugs)
// - generateFeatures(token, projectId, options): Generate features using AI

